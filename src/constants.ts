/**
 * Performance constants for signal rendering
 */

/**
 * Maximum number of bits to render without viewport slider
 * Beyond this threshold, a slider will be shown for navigation
 */
export const VIEWPORT_THRESHOLD = 50;

/**
 * Number of bits to show in the viewport at once
 */
export const VIEWPORT_WINDOW_SIZE = 50;

/**
 * Buffer size (in bits) to pre-calculate beyond visible window for smooth scrolling
 */
export const VIEWPORT_BUFFER_SIZE = 10;

